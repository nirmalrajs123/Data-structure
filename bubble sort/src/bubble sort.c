#include <stdio.h>
#include <stdlib.h>

int main(void) {
	int n,i,j,a[10],temp;
	puts("Enter the size");
	scanf("%d",&n);
	puts("Enter the element");
	for(i=0;i<n;i++){
		scanf("%d",&a[i]);
	}
	for(i=0;i<n-1;i++){
		for(j=i+1;j<n;j++){
			if(a[i]>a[j]){
				temp=a[i];
				a[i]=a[j];
				a[j]=temp;
			}
		}
	}
	puts("Sorted array");
	for(i=0;i<n;i++){
		printf("%d",a[i]);
	}
}
